package com.techelevator.deckOfCards;

public class Card {
	
	private String suit;
	private String value;
	
	public Card() {
		
	}
	
	public String getSuit() {
		return suit;
	}
	
	public void setSuit(String suit) {
		this.suit = suit;
	}
	
	public String getValue() {
		return value;
	}
	
	public void setValue(String value) {
		this.value = value;
	}
	

}
